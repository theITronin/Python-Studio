print("¡Hello World!") # The 'print' function displays text in the console.

print() # A 'print()' with no arguments prints an empty line.

print('¡Hello World!') # It works the same with single quotes (') or double quotes (").

print("Line 1\nLine 2") # '\n' creates a line break.

print("Text A", "Text B", "Text C") # Arguments are automatically separated by spaces.

print("Hello", end=" ") # end=" " prevents the line break and uses a space as a separator.
print("World")

print("a", "b", "c", sep="-") # sep customizes the separator between arguments.

print("x", "y", sep="|", end="*") # sep to separate and end for the end of the line.
print("z")